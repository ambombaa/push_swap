#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *str;

	str = (char *)s;
	while ((*str != '\0') && (*str != c))
		str++;
	if (*str == c)
		return ((char *)str);
	else
		return (NULL);
}
